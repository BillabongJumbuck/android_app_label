package tfidf

import "strings"

// MatchOEM checks OEM hard rules. Returns (label, true) on match.
func MatchOEM(appName, packageName string) (string, bool) {
	name := strings.TrimSpace(appName)

	// Keyword rules (longer keywords checked first within their group)
	for _, rule := range oemNameRules {
		if strings.Contains(name, rule.keyword) {
			return rule.label, true
		}
	}

	// Short name fallback
	if lbl, ok := oemShortName[name]; ok {
		return lbl, true
	}

	// OEM prefix only → generic OEM label
	if isOEMPkg(packageName) {
		return "OEM系统预装", true
	}

	return "", false
}

func isOEMPkg(pkg string) bool {
	for _, prefix := range oemPrefixes {
		if strings.HasPrefix(pkg, prefix) {
			return true
		}
	}
	return false
}

