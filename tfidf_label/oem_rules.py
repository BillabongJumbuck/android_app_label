def is_oem(package_name):
    for prefix in OEM_PKG_PREFIXES:
        if package_name.startswith(prefix):
            return True
    return False


def match(app_name, package_name=""):
    """
    硬规则匹配 label。
    返回 (label, confidence): 1.0=关键词命中, 0.8=短名称兜底, 0.3=仅OEM前缀, None=未命中
    """
    name = app_name.strip()

    # 1. 关键词匹配（按顺序，长关键词优先于短关键词）
    for keyword, label in NAME_RULES:
        if keyword in name:
            return (label, 1.0)

    # 2. 短名称精确兜底
    if name in SHORT_NAME_LABEL:
        return (SHORT_NAME_LABEL[name], 0.8)

    # 3. OEM 前缀暗示
    if is_oem(package_name):
        return ("OEM系统预装", 0.3)

    return (None, 0)
